/* Calcule el cuadrado de 2 números ingresados. */

const num1 = Number(prompt('Ingrese el primer número'))
const num2 = Number(prompt('Ingrese el segundo número '))

let resultado1 = num1 * num1
let resultado2 = num2 * num2

alert(`El cuadrado de el número ${num1} es ${resultado1} y el cuadrado de el número ${num2} es ${resultado2}`)